function battery_level = calc_battery_level(u)
    u1 = 8.17;
    l1 = 100;    
    u2 = 7.38;
    l2 = 50;
    u3 = 7.3;
    l3 = 12;
    u4 = 6.3;
    l4 = 0;

    if u >= u2 
        battery_level = min(100, l2 + (l1-l2)/(u1-u2) * (u-u2));
    elseif u > u3
        battery_level = l3 + (l2-l3)/(u2-u3) * (u-u3);
    else
        battery_level = max(0, l4 + (l3-l4)/(u3-u4) * (u-u4));
    end
end